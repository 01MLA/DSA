package icpc_boot_camps.third;

import java.util.Scanner;

public class Add {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        // Read two space-separated numbers
        int a = sc.nextInt();
        int b = sc.nextInt();

        // Add them
        int sum = a + b;

        // Print the result
        System.out.println(sum);
    }
}
